"use client";

import { useEffect, useRef, useState, type ReactNode } from "react";

// One-shot scroll-in entrance: adds .reveal-visible (see globals.css) the
// first time this element crosses the viewport, then disconnects — never
// re-triggers on scroll-up, so it doesn't feel jittery. Reduced-motion is
// handled entirely in CSS (.reveal renders as its final state under that
// media query), so no JS branching is needed here.
export function Reveal({
  children,
  className = "",
  delayMs = 0,
}: {
  children: ReactNode;
  className?: string;
  delayMs?: number;
}) {
  const ref = useRef<HTMLDivElement>(null);
  const [visible, setVisible] = useState(false);

  useEffect(() => {
    const node = ref.current;
    if (!node) return;

    const observer = new IntersectionObserver(
      ([entry]) => {
        if (entry.isIntersecting) {
          setVisible(true);
          observer.disconnect();
        }
      },
      { threshold: 0.15 },
    );

    observer.observe(node);
    return () => observer.disconnect();
  }, []);

  return (
    <div
      ref={ref}
      className={`reveal ${visible ? "reveal-visible" : ""} ${className}`}
      style={delayMs ? { transitionDelay: `${delayMs}ms` } : undefined}
    >
      {children}
    </div>
  );
}
